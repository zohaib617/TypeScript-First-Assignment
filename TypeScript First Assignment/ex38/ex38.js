function cityCountry(city, country) {
    return "".concat(city, ", ").concat(country);
}
// Call the function with different city-country pairs
var location1 = cityCountry('Karachi', 'Pakistan');
var location2 = cityCountry('Paris', 'France');
var location3 = cityCountry('New York', 'USA');
// Print the values returned by the function
console.log(location1);
console.log(location2);
console.log(location3);
